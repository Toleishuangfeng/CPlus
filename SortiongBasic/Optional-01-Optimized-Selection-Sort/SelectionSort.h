#pragma once
#include<iostream>
#include<algorithm>

using namespace std;
//选择排序算法
template<typename T>
void selectionSort(T arr[], int n) {
	for (int i = 0; i < n; i++) {
		int minIndex = i;
		//一轮循环找到最小的那个
		for (int j = i + 1; j < n ; j++)
			if(arr[j] < arr[minIndex])
				minIndex = j;
		swap(arr[i], arr[minIndex]);
	}
}